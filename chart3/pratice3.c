#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <string.h>

int main(void)
{
	// 3-1
	printf("예제3-1\n");
	int a;
	int b, c;
	double da;
	char ch;

	a = 10;
	b = a;
	c = a + 20;
	da = 3.5;
	ch = 'A';

	printf("변수 a의 값 : %d\n", a);
	printf("변수 b의 값 : %d\n", b);
	printf("변수 c의 값 : %d\n", c);
	printf("변수 da의 값 : %.1lf\n", da);
	printf("변수 ch의 값 : %c\n", ch);

	printf("char 크기 :%d\n", sizeof(char));
	printf("int 크기 :%d\n", sizeof(int));
	printf("\n");
	
	// 3-2
	printf("예제3-2\n");
	char ch1 = 'A';
	char ch2 = 65;

	printf("문자 %c의 아스키 코드 값 : %d\n", ch1, ch1);
	printf("아스키 코드 값이 %d인 문자 : %c\n", ch2, ch2);
	printf("\n");

	// 3-3
	printf("예제3-3\n");
	short sh = 32767;
	int in = 2147483647;
	long ln = 2147483647;
	long long lln = 123451234512345;

	printf("short형 변수 출력 : %d\n", sh);
	printf("int형 변수 출력 : %d\n", in);
	printf("long형 변수 출력 : %ld\n", ln);
	printf("long long형 변수 출력 : %lld\n", lln);
	printf("\n");

	// 3-4
	printf("예제3-4\n");
	unsigned int z;

	z = 4294967295;
	printf("%d\n", z);
	z = -1;
	printf("%u\n", z);
	printf("\n");

	// 3-5
	printf("예제3-5\n");
	float ft = 1.234567890123456789;
	double db = 1.234567890123456789;

	printf("float형 변수의 값 : %.20f\n", ft);
	printf("double형 변수의 값 : %.20lf\n", db);
	printf("\n");

	// 3-6
	printf("예제3-6\n");
	char fruit[20] = "strawberry";

	printf("딸기 : %s\n", fruit);
	printf("딸기쨈 : %s %s\n", fruit, "jam");
	printf("\n");

	// 3-7
	printf("예제3-7\n");
	printf("%s\n", fruit);
	strcpy(fruit, "banana");
	printf("%s\n", fruit);
	printf("\n");

	//3-8
	printf("예제3-8\n");
	int income = 0;
	double tax;
	const double tax_rate = 0.12;
	
	income = 456;
	tax = income * tax_rate;
	printf("세금은 : %.1lf입니다.\n", tax);
	printf("\n");

	// 3-9
	printf("예제3-9\n");
	int y;
	printf("정수 하나를 입력하세요 : ");
	scanf("%d", &y);
	printf("입력된 값 : %d\n", y);
	printf("\n");
	
	// 3-10
	printf("예제3-10\n");
	int age;
	double height;

	printf("나이와 키를 입력하세요 : ");
	scanf("%d%lf", &age, &height);
	printf("나이는 %d살, 키는 %.1lfcm입니다.\n", age, height);
	printf("\n");
	
	// 3-11
	printf("예제3-11\n");
	char grade;
	char name[20];

	//while (getchar() != '\n');

	printf("학점 입력 : ");
	scanf(" %c", &grade);
	printf("이름 입력 : ");
	scanf("%s", name);
	printf("%s의 학점은 %c입니다.\n", name, grade);
	printf("\n");

	// 3-etc1
	printf("예제3-etc1\n");
	char address[50];
	char name1[20];
	int age1;

	printf("이름 입력 : ");
	scanf("%s", name1);
	printf("나이 입력 : ");
	scanf("%d", &age1);
	printf("주소 입력 : ");
	scanf("%s", address);
	printf("%s의 나이는 %d이고 주소는 %s입니다.\n", name1, age1, address);
	printf("\n");
	
	// 3-etc2
	printf("예제3-etc2\n");
	int a1;
	char b1;
	
	printf("정수 하나를 입력하세요 : ");
	scanf("%d", &a1);
	printf("문자 하나를 입력하세요 : ");
	scanf(" %c", &b1);
	printf("입력된 값은 %d, %c 입니다.\n", a1, b1);
	printf("\n");

	return 0;
}